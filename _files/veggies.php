		<h4>Calcium & Phosphorous Content of Common Vegetables & Fruits (Adapted from "Food Values of Portions Commonly Used", by Bowes & Church, revised by Jean Pennington, 15th Ed.)</h4>
		
		
		
		<p class="veggieInfo">The following information lists how many milligrams of calcium & phosphorous ar contained in a 1 cup portion of each food item. Proper health and bone development depend not only on adequate amounts of calcium, but also on a proper calcium to phosphorous ratio; ideally this should be between 1.5 : 1 and 2 : 1. This means that you need to have 1.5 to 2 times as much calcium as phosphorous in the diet; other wise your pet may develop bone disease and other problems, even if the calcium levels in the diet are fairly high. The individual food items don't always have to have an ideal Ca : P ratio, but the AVERAGE value of the overall diet needs to be in balance. Feeding a variety of foods is the best way to avoid problems with any nutrients, not just calcium and phosphorous. Concentrate on feeding items from the "Ideal Ratio" list, along with items from the "High Ratio" list and "Moderate Ratio" lists; give only small portions of the items in the "Poor Ca/P Ratio" list (no more than 10-15% of the total veggies fed.) Especially minimize items in the "Poor Ca/P Ratio" list which have very high phosphorous levels, i.e. over 80mg phosphorous per cup of food.</p>
		
		<p class="veggieInfo">For pets with diagnosed kidney disease, as is common with Iguanas, the priorities become different. Diseased kidneys tend to retain phosphorous, so levels of this mineral become very high in the blood and tissues. The diet in these patients should be as low as possible in overall phosphorous, regardless of the Ca : P ratio. Therefore select foods from the "Ideal Ratio", "High Ratio", and "Moderate Ratio" lists which have a total phosphorous content of less than 30mg per cup of food. High phosphorous content is dangerous, even if the Ca : P ratio is ideal! For instance, plums would be safer to feed a kidney patient than beet greens, despite the fact that plums have a poor Ca : P ratio while beets have a great Ca : P ratio! Use no supplements which contain ANY phosphorous. Often a phosphate binder (such as Tums) is fed with the meal; this minimizes absorption of phosphorous from the food.</p>

