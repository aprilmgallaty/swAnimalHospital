	<div id="tabs-7">
		<h3>Common Diseases</h3>
		<dl>
			<dd>&rarr; &nbsp;<span class="special">Obesity&#58;</span></dd>
			<dd>Obesity is common in pet prairie dogs, due to inadequate exercise (they may run all day in a natural setting) and due to overfeeding with high calorie diets.  Obesity may increase the risk of other health problems.  Moderate daily exercise and restriction of pellet intake are recommended. Avoid all high calorie treats (see diet).</dd>
			
			<dd>&rarr; &nbsp;<span class="special">Respiratory Disease&#58;</span></dd>
			<dd>Prairie dogs may develop bacterial (and possibly fungal) respiratory infections with exposure to other animals or moist soiled cage conditions.  Dusty bedding or fumes from cedar shavings may irritate the respiratory system also.  Signs may include wheezing, labored breathing, lethargy, appetite loss, and nasal or eye discharge.  Treatment is with antibiotics and elimination of underlying causes such as dirty cage conditions, poor diet, etc.</dd>
			
			<dd>NOTE: Dental disease and heart disease cause similar symptoms; suspect these problems whenever respiratory symptoms fail to respond to antibiotics.  Dental disease is a very common cause of respiratory distress in prairie dogs.</dd>
			
			<dd>&rarr; &nbsp;<span class="special">Heart Disease&#58;</span></dd>
			<dd>This is fairly common in prairie dogs and may be associated with high calcium diets or obesity.  Signs mimic respiratory infection and may include lethargy, wheezing, labored breathing, bloated appearance, cold extremities, or sudden collapse.  This disease isn't curable but medication can reduce symptoms for long periods in some cases.  Improving the diet and reducing obesity (when present) may help prolong life.</dd>
			
			<dd>&rarr; &nbsp;<span class="special">Dental Disease&#58;</span></dd>
			<dd>Prairie dogs are prone to dental problems, in part due to excessive cage bar biting.  Tooth trauma and other factors can result in malocclusion or crooked teeth, usually involving the front teeth (incisors).  Signs include grossly overgrown crooked teeth, difficulty eating, drooling, and oral odor.  Treatment is via trimming the overgrown teeth; this may be needed every 3-5 weeks lifelong unless the teeth are extracted. </dd>
			<dd>The other common tooth problem is a tumor growing on the tooth root (odontoma), usually involving one or both upper incisors.  The tooth develops a bony growth or swelling at the root tip, likely from impact trauma as with cage bar biting.  The tooth root is located in the nasal passage, so the tumor slowly blocks airflow, eventually resulting in extreme difficulty breathing.  Prairie dogs do not breathe well through the mouth, so a nasal blockage is life threatening.  They may choke and inhale food or water, resulting in an aspiration pneumonia which worsens the symptoms.  The affected incisor may be worn short (less than 1cm long) due to lack of normal growth.  Diagnosis is via x-rays of the head; treatment is via extraction of the affected tooth or teeth.  This is a difficult disease to treat and not all patients survive.  Try to avoid this problem by providing a large cage and using soft cardboard or rope toys to minimize biting on metal.  (See Housing).</dd>
			
			<dd>&rarr; &nbsp;<span class="special">Ringworm (Fungal Infection)&#58;</span></dd>
			<dd>Prairie dogs may get fungal infections of the skin; this can occasionally be transmitted to other pets or humans.  Signs include hair loss, and scaly, thickened, or darkened skin on the chest, abdomen, lower back, tail or head.  Itching is minimal.  Diagnosis is via culturing the hair for fungus.  Treatment is via topical and systemic antifungal drugs.</dd>
			
			<dd>&rarr; &nbsp;<span class="special">Uterine Infections and Cancer&#58;</span></dd>
			<dd>Uterine disease occurs in middle aged or older females; both infections and cancers may be seen. b Signs of uterine disease may be subtle, and include vaginal discharge, odor, weight loss, or lethargy.  The best prevention is spaying when young (8-12 months old).  This also prevents unwanted pregnancy if a pair is housed together.</dd>
		</dl>
