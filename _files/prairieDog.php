		<h3>Diet</h3>
			<p>The prairie dog's natural diet is mostly grasses.  In captivity the diet should consist mostly of grass hay such as timothy hay (not alfalfa), and commercial prairie dog pellets.  If a prairie dog diet isn't available you may substitute a pelleted guinea pig diet, or a low calcium rabbit pellet such as Oxbox Bunny Basics-T or Kaytee Exact.  The hay is fed free choice always; the pellets should be rationed at &frac14; to 1/3 cup per pet daily to minimize obesity.  Avoid mixes which contain seeds &amp; nuts, dried fruits, etc. as these may cause obesity or bowel upsets.  Fresh vegetables, mostly leafy greens, may be added to the diet (up to 10-15% of the total food intake daily).  Minimize vegies in the cabbage family as they may cause digestive upsets.  Restrict feeding of sweet items (fruit, corn, baby carrots, tomatoes, etc) and high protein items (seeds, oats, alfalfa, etc) as these may cause bowel upsets also.  Provide fresh water always; a rodent water bottle is cleaner than a bowl and can't be spilled.  Clean the water bottle every 1-2 days.</p>
	</div><!-- end #tabs-3 -->
	
	<div id="tabs-4">
		<h3>Behavior</h3>
		<p>With maturity prairie dogs may become more aggressive and difficult to handle. Frequent handling will help keep them tame, and some well-socialized individuals become rather docile.  Neutered animals may show less aggression and odor when mature.  Nonetheless adult prairie dogs may bite when upset or excited so they should be handled with care. They may gnaw on objects in their environment like many rodents, so must be prevented from damaging wood furniture, baseboards, extension cords, etc.  They typically must be confined in a cage when not observed to minimize house damage and maximize safety for the pet.  However, like most wildlife they are bothered by confinement and may spend excessive amounts of time gnawing at cage bars due to boredom or attempts to escape.  This can cause permanent damage to their teeth.  (See housing for suggestions to minimize this behavior).</p>
		
	</div><!-- end #tabs-4 -->
	
	<div id="tabs-5">
		<h3>Handling</h3>
		<p>To lift a prairie dog one should gently wrap one hand around the chest from above while supporting the hindquarters with your other hand.   Keep your hands behind the head as prairie dogs may bite when upset or agitated.  They also have sharp claws.  A thick towel may be wrapped around the animal for better control and protection.  Covering the head with a towel may make restraint easier for procedures such as nail trimming.  Nails should be trimmed every 2-4 weeks depending on growth rate; small pet trimmers or human nail trimmers can be used.</p>
		
	</div><!-- end #tabs-5 -->
	
	<div id="tabs-6">
		<h3>Housing</h3>
		<p>Prairie dogs may be housed in wire, hard plastic or glass enclosures.  Solid wall cages must be well ventilated or they can have problems with excess heat and humidity, and buildup of ammonia fumes from urine.  Wood is hard to clean and may be gnawed through.  The floors can be solid or wire.  Wire bottoms allow waste to fall through, but can cause foot sores; provide a solid platform (wood, plastic, or cardboard) in one area to sit on, which can be replaced if chewed or soiled.  Heavy straw can be layered over a wire bottom to provide a cushion also.</p>
		<p>Solid floors should be covered with straw or shredded paper bedding to absorb wastes; keep the floor clean and dry.  Alternate absorbent beddings include hardwood shavings (not pine or cedar), corncob (sterilized to kill fungus), or commercial paper pellets.  Prairie dogs are social so more than one may live together, but not all animals socialize well, and ideally introductions should be at a young age.  Adult males may fight if not neutered.  Provide cardboard chew toys (like toilet paper roll or paper towel roll cores) to gnaw on; avoid wood or metal toys as chewing on these may cause tooth damage.  A hanging length of _ inch thick nylon rope provides a chew toy as well, and may minimize cage bar biting which is a common behavioral problem of captive prairie dogs.  Large cages may reduce harmful escape behaviors.
</p>
		
