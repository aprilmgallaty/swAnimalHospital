		<h3>General</h3>
			<p>Prairie dogs are heavy bodied rodents native to the grassy plains of North America from the Dakotas south to northern Mexico.  Of the five species of prairie dog (genus Cynomys ) which reside in the United States, the black-tailed prairie dog (Cynomys ludovicianus) is the most common in captivity.  In nature prairie dogs live in colonies and form extensive burrows underground.  They tend to be active during the day, and in nature may run and play for hours.  Black tailed prairie dogs are stocky and brown, and adults weigh 1&frac12; to 2&frac12; pounds, occasionally more. </p>
